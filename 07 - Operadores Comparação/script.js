//  == igual a
//  === valor e tipo igual a
//  != diferente de
//  !== valor e tipo diferente de 
//  > maior que
//  < menor que
//  >= maior ou igual a
// <= menor ou igual a 

var a = 25
var b = 24
var c = "25"

console.log(a, " é igual == a ", b, ": ", a == b)
console.log(a, " é igual == a ", c, ": ", a == c)
console.log(a, " é igual === a ", c, ": ", a === c)
console.log(a, " é diferente != de ", b, ": ", a != b)
console.log(a, " é diferente != de ", c, ": ", a != c)
console.log(a, " é diferente !== de ", c, ": ", a !== c)
console.log(a, " é maior > que ", b, ": ", a > b)
console.log(a, " é menor < que ", b, ": ", a < b)
console.log(a, " é maior ou igual >= que ", a, ": ", a >= a)
console.log(a, " é menor ou igual >= que ", a, ": ", a <= a)